// Предложите пользователю ввести номер
let n = parseInt(prompt("Введите число (N):"));

// Инициализируйте пустой массив для хранения четных чисел
let evenNumbers = [];

// Итерация от 2 до N (включительно)
for (let i = 2; i <= n; i++) {
  // If the number is even, add it to the array
  if (i % 2 === 0) {
    evenNumbers.push(i);
  }
  // Если N нечетное, вычтите 1 из условия цикла
  if (n % 2 !== 0) {
    n--;
  }
}

//Объедините и выведите на экран четные числа
console.log(evenNumbers.join(" "));

// Этот код выполняет задачу, используя простой forцикл и условные операторы.
// Пользовательский ввод сначала преобразуется в целое число, а затем цикл повторяется от 2 до N, добавляя четные числа в массив с именем evenNumbers.
// Если N нечетно, вместо этого условие цикла корректируется для итерации до N-1. Наконец, joinметод используется для отображения всех четных чисел, разделенных пробелами.